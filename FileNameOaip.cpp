#include <iostream>
using namespace std;

int main() {
    setlocale(LC_ALL, "RU");
        const int N = 10;
    int a[N];

    // Ввод элементов
    cout << "Введите " << N << " целых чисел (через Enter или пробел)::\n";
    for (int i = 0; i < N; ++i) {
        cout << "a[" << i << "] = ";
        cin >> a[i];
    }

    // Вывод введённого массива
    cout << "\nВведённый массив:\n";
    for (int i = 0; i < N; ++i) cout << a[i] << (i < N - 1 ? " " : "\n");

    // Сумма элементов
    int sum = 0;
    for (int i = 0; i < N; ++i) sum += a[i];
    cout << "Сумма элементов массива: " << sum << "\n";

    // Нахождение максимального элемента
    int mx = a[0];
    for (int i = 1; i < N; ++i)
        if (a[i] > mx) mx = a[i];
    cout << "Максимальный элемент: " << mx << "\n";

    // Сортировка вставками в порядке убывания
    // Идея: для каждого i (1..N-1) вставляем a[i] в уже отсортированную часть a[0..i-1]
    for (int i = 1; i < N; ++i) {
        int key = a[i];
        int j = i - 1;
        // Для убывания: сдвигаем элементы, меньшие чем key, вправо
        while (j >= 0 && a[j] < key) {
            a[j + 1] = a[j];
            --j;
        }
        a[j + 1] = key;
    }

    // Вывод отсортированного массива
    cout << "\nМассив после сортировки (по убыванию):\n";
    for (int i = 0; i < N; ++i) cout << a[i] << (i < N - 1 ? " " : "\n");

    return 0;

}
